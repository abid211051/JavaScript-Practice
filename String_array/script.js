//???????????????????????????--string, Integer, float value check in an array--????????????????????????????????//
console.log("<----OUTPUT FOR STRING FLOAT OR INTEGER CHECKING ---->")
const arr0 = [1, 2, 3, 'A', 'B', 'C', 3.1, 1, 2.4, 'D']
var arr1 = []
var arr2 = []
var arr3 = []
arr0.forEach(element => {
    if (typeof (element) == "string") {
        arr1.push(element);
    }
    else if (typeof (element) == "number") {
        if (Number.isInteger(element)) {
            arr2.push(element);
        }
        else {
            arr3.push(element)
        }
    }
});
console.log(arr1);
console.log(arr2);
